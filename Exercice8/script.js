/**
 * Additionne deux nombres
 * @param {number|string} a - Premier nombre (entier ou décimal)
 * @param {number|string} b - Deuxième nombre (entier ou décimal)
 * @returns {number} La somme des deux nombres
 */
function add(a, b) {
    return Number(a) + Number(b);
  }
  
 export default add;
